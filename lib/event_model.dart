class Event {
  String name;
  String description;
  String type;
  String date;
  String time;
  String address;
  String organizer;
  String contact;

  Event({
    required this.name,
    required this.description,
    required this.type,
    required this.date,
    required this.time,
    required this.address,
    required this.organizer,
    required this.contact,
  });
}
